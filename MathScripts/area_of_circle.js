//calculates area of a circle
// formula for calculating the area of a circle is: pi * radius^2 = area

//radius of circle
let radiusOfCircle = 10;
//pi
let pi = Math.PI;

//the equation to get the area of the circle 
const areaOfCircle = pi * radiusOfCircle * radiusOfCircle;

//outputs the area of the circle
console.log("The area of a circle with a radius of "+ radiusOfCircle +" is "+ areaOfCircle.toFixed(2));

